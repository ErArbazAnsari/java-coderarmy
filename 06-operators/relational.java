public class relational {
    public static void main(String[] args) {
        System.err.println("relational operators (==, !=, >, >=, <, <=)\n");

        int a = 10;
        int b = 20;

        System.err.println("a=" + a + " b=" + b);
        System.err.println("a==b " + (a == b));
        System.err.println("a!=b " + (a != b));
        System.err.println("a>b " + (a > b));
        System.err.println("a>=b " + (a >= b));
        System.err.println("a<b " + (a < b));
        System.err.println("a<=b " + (a <= b));
    }
}
